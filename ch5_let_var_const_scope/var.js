// ------------------ VAR examples ------------------

// 1
var k = 10;
k = 15;
console.log("var k:", k);

// 2
var l;
l = "World";
console.log("var l:", l);

// 3
var m = true;
m = !m;
console.log("var m:", m);

// 4
var n = 50;
n -= 20;
console.log("var n:", n);

// 5
var o = [10];
o.push(20);
console.log("var o:", o);

// 6
var p = { age: 25 };
p.age = 30;
console.log("var p:", p);

// 7
var q = 2;
q *= 3;
console.log("var q:", q);

// 8
var r = "JS";
r = r + " Rocks";
console.log("var r:", r);

// 9
var s = undefined;
s = "Defined now";
console.log("var s:", s);

// 10
var t = 100;
t--;
console.log("var t:", t);
